function drawTiles() {
    let r;
    const tileDiv = document.getElementById('tiles');
    for (r = 0; r < guessRows.length; r += 1) {
        const rowElem = document.createElement('div');
        rowElem.setAttribute('id', 'row-' + r);

        let curRow = guessRows[r];
        for (c = 0; c < curRow.length; c += 1) {
            const tileElem = document.createElement('div');
            tileElem.setAttribute('id', 'row-' + r + '-tile-' + c);
            tileElem.classList.add('tile');
            rowElem.append(tileElem);
        }

        tileDiv.append(rowElem);
    }
}

